namespace CursoGithub.Clases
{
    /// <summary>
    /// Clase principal que contiene la definición de la clase Empleado.
    /// </summary>
    public class RenanValencia
    {
        /// <summary>
        /// Clase que representa a un empleado con varios atributos.
        /// </summary>
        public class Empleado
        {
            /// <summary>
            /// Obtiene o establece el nombre del empleado.
            /// </summary>
            public string Nombre { get; set; }

            /// <summary>
            /// Obtiene o establece el apellido del empleado.
            /// </summary>
            public string Apellido { get; set; }

            /// <summary>
            /// Obtiene o establece la edad del empleado.
            /// </summary>
            public int Edad { get; set; }

            /// <summary>
            /// Obtiene o establece el salario del empleado.
            /// </summary>
            public double Salario { get; set; }

            /// <summary>
            /// Obtiene o establece el cargo del empleado.
            /// </summary>
            public string Cargo { get; set; }

            /// <summary>
            /// Obtiene o establece el documento del empleado.
            /// </summary>
            public string Documento { get; set; }

            /// <summary>
            /// Obtiene o establece el teléfono del empleado.
            /// </summary>
            public string Telefono { get; set; }
        }
    }
}

